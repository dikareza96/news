<p><strong>Berita Poltek</strong>&nbsp;| Hari ini tanggal 23 April 2012 merupakan hari bersejarah bagi segenap civitas akademika Politeknik Kediri, karena bertepatan dengan dilaksanakannya acara serah terima jabatan (SERTIJAB) Direktur Politeknik Kediri dari Direktur lama Drs. H. M. Zaini, MM kepada Direktur baru Drs. Bambang Soekodiono, MT., sesuai dengan Surat Keputusan Walikota Kediri dr. H. Samsul Ashar,Sp.Pd. Nomor: 045.2/628/419.16/2012 tanggal 16 April 2012.</p>
<p>Dengan dihadiri tamu undangan Plt. Ketua Yayasan Pendidikan Tinggi Joyoboyo Kediri Drs. Adi Wiyono, M.Si. dan Sekretaris Yayasan Bapak Drs. H. Syaiful Yasin, MM serta seluruh jajaran Senat, kepala bagian dan kepala UPT Politeknik Kediri, prosesi berlangsung dengan hidmat.&nbsp; Setelah acara pembukaan, Bapak Drs. H.M. Zaini, MM., selaku Direktur lama Politeknik Kediri menyampaikan laporan pertanggungjawaban selama menjabat sebagai Direktur sejak tahun 2008 &ndash; 2012, kemudian dilanjutkan dengan prosesi serah terima jabatan yang disaksikan Sekretaris Yayasan Pendidikan Tinggi Joyoboyo Kediri. Selanjutnya Drs. Bambang Soekodiono, MT selaku Pelaksana tugas Direktur yang baru berkesempatan menyampaikan sambutan.&nbsp; Beliau menyatakan bahwa ini adalah suatu amanah yang akan dijalankan dengan sungguh-sungguh, meneruskan dan mengembangkan program-program yang telah dirintis oleh Direktur lama, dan mengharap dukungan seluruh&nbsp; civitas akademika Politeknik Kediri, untuk dapat bekerjasama dan bersinergi mewujudkan Politeknik Kediri yang lebih baik kedepan menuju Politeknik Negeri Kediri.</p>
<p>Semangat dan doa penuh harapan untuk masa depan Politeknik Kediri lebih baik terpancar dari seluruh civitas akademika, mewarnai acara serah terima jabatan Politeknik Kediri kali ini.&nbsp; Semoga Allah SWT. Selalu memberikan ridlo, keberkahan, kesuksesan, kemudahan dan kejayaan untuk semua cita-cita warga Politeknik Kediri&hellip;.Amiiiiin Ya Robbal &lsquo;alamin. (bapsi.ema)</p>
<p>&nbsp;=======================================================================================</p>
<p><span  underline;"><strong>VISI, MISI, TUJUAN dan KULTUR NILAI POLITEKNIK KEDIRI</strong></span></p>
<p><strong>VISI</strong> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; : Menjadi institusi pendidikan vokasi yang berkualitas dalam mengembangkan teknologi rekayasa dan tata niaga secara terintegrasi.</p>
<p><strong>MISI</strong> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; :</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;1. Menyelenggarakan pendidikan vokasi yang inovatif</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;2. Meningkatkan Kinerja dan hasil penelitian berbasis standar mutu</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;3. Meningkatkan layanan dan kerjasama berbasis teknologi informasi,</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;4. meningkatkan tata kelola,</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5. mewujudukan insan yang berkarakter dan berwenang kebangsaan</p>
<p><strong>Tujuan &nbsp;</strong> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; :&nbsp;</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;1. Terselenggaranya pendidikan vokasi yang inovatif</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;2. Meningkatnya kinerja dan hasil penelitian berbasis standar mutu</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;3. Menigkatnya layanan dan kerjasama berbasis teknologi informasi</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;4. Meningkatnya Efektivitas dan efisiensi tata kelola</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5. Terwujudnya insan yang berkarakter dan berwawasan kebangsaan</p>
<p><strong>Kultur Nilai</strong> &nbsp; &nbsp; &nbsp; &nbsp; : SHINE</p>
<p><strong>Filosofi &nbsp;</strong> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; : S = Smart : Cerdas sosial, emosional dan intelektual</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;H = Humanity : manusiawi dan bijaksana dalam bertindak</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;I &nbsp;= Integrity : jujur dan tulus dalam setiap tindakan</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;N = Nationality : berkarakter berwawasan kebangsaan</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;E = Excellence : berorientasi pada kualitas dalam berkarya</p>
